	public int getFromGrade(){
		int gr = 100;
		if(from_grade != null && !from_grade.isEmpty()){
			gr = getGrade(from_grade);
		}
		
		return gr;
	}
	
	public int getToGrade(){
		int gr = 100;
		if(to_grade != null && !to_grade.isEmpty()){
			gr = getGrade(to_grade);
		}
		
		return gr;
	}
	
	private int getGrade(String grade){
		int gr = 100;
		if(grade.equalsIgnoreCase("Pre-school")){
			gr = -2;
		}else if(grade.equals("Pre-Kindergarten") || grade.equalsIgnoreCase("pre k")){
			gr = -1;
		}else if(grade.equals("Kindergarten") || grade.equalsIgnoreCase("K")){
			gr = 0;
		}else if(!grade.isEmpty()){
			grade = grade.replaceAll("[\\D]", "");
			try{
				gr = Integer.parseInt(grade);
			}catch(Exception e){
				e.printStackTrace();
			}
		}
		
		return gr;
	}
	
	public String getSanitizedYesNo(String val){
		if(val.equals("y") || val.equalsIgnoreCase("yes"))
			return "Y";
		else if(val.equals("n") || val.equalsIgnoreCase("no"))
			return "N";
		else 
			return val;
	}
	
